            event.preventDefault();

            var data = new FormData(this);

            document.getElementById("btn").disabled = true;
            document.getElementById("btn").innerHTML = "Enviando, aguarde...";

            $.ajax({
                url: '<?= base_url(); ?>ajax/cadastro-lp',
                type: 'POST',
                dataType: 'JSON',
                data:  data,
                mimeType:"multipart/form-data",
                contentType: false,
                cache: false,
                processData:false,
                success: function(x)
                {
                    document.getElementById("btn").disabled = false;
                    document.getElementById("btn").innerHTML = "ENVIAR";


                    if(x.tipo == true)
                    {
                        Swal.fire({
                            type: 'success',
                            title: 'Sucesso!',
                            text: x.mensagem
                        });

                        // Limpa os Campos dos formulários
                        $('#formCadastroLP').each (function(){this.reset();});
                    }
                    else 
                    {
                        Swal.fire({
                            type: 'error',
                            title: 'Oops...',
                            text: x.mensagem
                        });
                    }
                }
            });

            return false;
        });


